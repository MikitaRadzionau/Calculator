function sum(a, b) {
    let result = a + b;
    return result;
}

function min(a, b) {
    let result = a - b;
    return result;
}

function mult(a, b) {
    let result = a * b;
    return result;
}

function div(a, b) {
    let result = a / b;
    return result;
}
module.exports = {
    sum,
    min,
    mult,
    div,
}